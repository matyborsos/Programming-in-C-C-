#include <stdio.h>

int main() {

    char c;

    scanf("%c", &c);

    printf("character=%c\n", c);
    // prints the char as character
    printf("decimal=%d\n", c);
    // prints the char in decimal notation.
    printf("octal=%o\n", c);
    // prints the char in octal notation.
    printf("hexadecimal=%x\n", c);
    // prints the char in hexadecimal notation.

    return 0;

}
